-- 4 ------------------------------
SELECT NAME AS "PRODUCT NAME OF TOP 3"
FROM
  (
    SELECT
      PRODUCT.NAME,
      count(PRODUCT.NAME)
    FROM SELLING
      INNER JOIN PRODUCT
        ON SELLING.PRODUCT_ID = PRODUCT.ID
    WHERE SELE_DATE BETWEEN to_date('1-10-2016', 'dd-mm-yyyy') AND to_date('1-11-2017', 'dd-mm-yyyy')
    GROUP BY PRODUCT.NAME
    ORDER BY count(PRODUCT.NAME) DESC
  )
WHERE ROWNUM <= 3;
