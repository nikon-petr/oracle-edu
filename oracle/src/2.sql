-- 6 ------------------------------
SELECT
  nvl(PRODUCT_TYPE.NAME, 'All Types')                                "PRODUCT TYPE",
  nvl((SELLER.FIRST_NAME || ' ' || SELLER.LAST_NAME), 'All Sellers') "SELLER NAME",
  sum(SELLING.PRICE * SELLING.QUANTITY)                              "AMOUNT"
FROM PRODUCT_TYPE
  INNER JOIN PRODUCT
    ON PRODUCT_TYPE.ID = PRODUCT.PRODUCT_TYPE_ID
  INNER JOIN SELLING
    ON PRODUCT.ID = SELLING.PRODUCT_ID
  INNER JOIN SELLER
    ON SELLING.SELLER_ID = SELLER.ID
WHERE SELLING.SELE_DATE BETWEEN to_date('1-10-2016', 'dd-mm-yyyy') AND to_date('1-11-2017', 'dd-mm-yyyy')
GROUP BY ROLLUP (PRODUCT_TYPE.NAME, (SELLER.FIRST_NAME || ' ' || SELLER.LAST_NAME))
ORDER BY PRODUCT_TYPE.NAME, (SELLER.FIRST_NAME || ' ' || SELLER.LAST_NAME);
-- --------------------------------

-- 7 ------------------------------
SELECT
  nvl(PRODUCT_TYPE.NAME, 'All Types')                                "PRODUCT TYPE",
  nvl((SELLER.FIRST_NAME || ' ' || SELLER.LAST_NAME), 'All Sellers') "SELLER NAME",
  sum(SELLING.PRICE * SELLING.QUANTITY)                              "AMOUNT"
FROM PRODUCT_TYPE
  INNER JOIN PRODUCT
    ON PRODUCT_TYPE.ID = PRODUCT.PRODUCT_TYPE_ID
  INNER JOIN SELLING
    ON PRODUCT.ID = SELLING.PRODUCT_ID
  INNER JOIN SELLER
    ON SELLING.SELLER_ID = SELLER.ID
WHERE SELLING.SELE_DATE BETWEEN to_date('1-10-2016', 'dd-mm-yyyy') AND to_date('1-11-2017', 'dd-mm-yyyy')
GROUP BY CUBE (PRODUCT_TYPE.NAME, (SELLER.FIRST_NAME || ' ' || SELLER.LAST_NAME))
ORDER BY PRODUCT_TYPE.NAME, (SELLER.FIRST_NAME || ' ' || SELLER.LAST_NAME);
