-- 3 ------------------------------
SELECT PRODUCT_TYPE.NAME
FROM PRODUCT_TYPE
  INNER JOIN PRODUCT
    ON PRODUCT_TYPE.ID = PRODUCT.PRODUCT_TYPE_ID
GROUP BY PRODUCT_TYPE.NAME;
