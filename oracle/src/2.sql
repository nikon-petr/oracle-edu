SELECT
  PRODUCT.NAME,
  PRICE,
  nvl(TARE.NAME, 'unknown')         AS TARE,
  nvl(PRODUCT_TYPE.NAME, 'unknown') AS TYPE
FROM PRODUCT
  LEFT OUTER JOIN TARE
    ON PRODUCT.TARE_ID = TARE.ID
  LEFT JOIN PRODUCT_TYPE
    ON PRODUCT.PRODUCT_TYPE_ID = PRODUCT_TYPE.ID;
